#!/usr/bin/env node

/**
 * Normalize docs and XML doc comments to ASCII.
 *
 * Targets:
 *   - All .md files
 *   - All .cs files under Runtime/, Editor/, Tests/, SourceGenerators/ -- but
 *     only the contents of /// XML doc comment lines are modified.
 *
 * The substitution table is documented inline below. Most replacements are
 * unconditional; bullets and arrows are only replaced outside fenced code
 * blocks. Box-drawing characters (U+2500-U+257F) are NEVER auto-replaced --
 * the script reports them and exits non-zero so a human can rewrite the
 * affected diagrams.
 *
 * Real emojis (codepoint >= U+1F300) are preserved. A per-file cap of 5 is
 * enforced as a warning only; Phase 3 will codify the policy.
 *
 * Usage:
 *   node scripts/normalize-docs-ascii.js [--check] [--paths <glob>] [files...]
 *
 * Options:
 *   --check        Exit non-zero if changes would be made; do not write.
 *   --paths <pat>  Comma-separated list of explicit file paths or directory
 *                  roots to scan. If omitted, the default scan set is used.
 *   files...       Explicit file paths take precedence over --paths.
 *
 * Exit codes:
 *   0  Success (no changes needed, or changes written).
 *   1  --check failed (changes would be made), or unrecoverable error.
 *   2  Box-drawing characters detected -- manual rewrite required.
 */

"use strict";

const fs = require("fs");
const path = require("path");

const ROOT_DIR = path.resolve(__dirname, "..");

const EXCLUDE_DIRS = new Set([
  ".git",
  "node_modules",
  "Library",
  "Temp",
  "obj",
  "bin",
  "Logs",
  "site",
  "coverage",
  "__pycache__"
]);

// Files that are regenerated by tooling and therefore skipped by the
// normalizer. The cleanup of any non-ASCII content emitted by the generators
// must be handled by updating the generators themselves (out of scope for
// Phase 2 mechanical cleanup).
const GENERATED_FILE_PATHS = new Set([
  path.join(ROOT_DIR, "llms.txt"),
  path.join(ROOT_DIR, ".llm", "skills", "index.md")
]);

const CS_SCAN_ROOTS = ["Runtime", "Editor", "Tests", "SourceGenerators"];

// --- Substitution table -----------------------------------------------------

// Substitutions that apply everywhere (including inside fenced code blocks
// and inline code spans -- curly quotes, em-dashes, ellipses, nbsp etc. are
// always wrong in code).
const UNCONDITIONAL_SUBS = [
  { from: /—/g, to: " -- ", id: "em-dash" }, // U+2014 EM DASH
  { from: /–/g, to: "-", id: "en-dash" }, // U+2013 EN DASH
  { from: /‑/g, to: "-", id: "non-breaking-hyphen" }, // U+2011
  { from: /[“”]/g, to: '"', id: "curly-double-quote" },
  { from: /[‘’]/g, to: "'", id: "curly-single-quote" },
  { from: /…/g, to: "...", id: "ellipsis" },
  { from: /×/g, to: "x", id: "multiplication-sign" },
  { from: /≤/g, to: "<=", id: "less-equal" }, // U+2264
  { from: /≥/g, to: ">=", id: "greater-equal" }, // U+2265
  { from: /≠/g, to: "!=", id: "not-equal" }, // U+2260
  { from: /²/g, to: "^2", id: "superscript-two" }, // U+00B2
  { from: /³/g, to: "^3", id: "superscript-three" }, // U+00B3
  { from: /±/g, to: "+/-", id: "plus-minus" }, // U+00B1
  { from: /›/g, to: ">", id: "single-right-angle" }, // U+203A
  { from: /‹/g, to: "<", id: "single-left-angle" }, // U+2039
  { from: /«/g, to: '"', id: "left-double-angle" }, // U+00AB
  { from: /»/g, to: '"', id: "right-double-angle" }, // U+00BB
  { from: / /g, to: " ", id: "nbsp" }
];

// Substitutions only applied outside fenced code blocks.
const PROSE_ONLY_SUBS = [
  { from: /•/g, to: "-", id: "bullet" } // U+2022 BULLET
];

// Arrow chars: substituted with context awareness. The replacement choice
// depends on local context to avoid producing a leading ">" (which would
// create a markdown blockquote) or breaking link text inside "[]".
const ARROW_RIGHT_RE = /[→⇒]/g; // -> and =>
const ARROW_LEFT_RE = /[←⇐]/g; // <- and <=
const ARROW_BOTH_RE = /↔/g; // <->
const ARROW_UP_RE = /[↑]/g; // ^

// Check / cross / warning chars.
const CHECK_RE = /[✓✅]/g;
const CROSS_RE = /[✗✘❌]/g;
const WARNING_RE = /[⚠]️?/g;

// --- Detection sets ---------------------------------------------------------

const BOX_DRAWING_RE = /[─-╿]/;
const DINGBAT_RANGE_RE = /[⌀-➿]/;

// Codepoints in the dingbat range that we already handle elsewhere.
const HANDLED_DINGBATS = new Set([
  0x2192,
  0x21d2,
  0x2190,
  0x21d0,
  0x2194,
  0x2191, // arrows
  0x2713,
  0x2705, // checks
  0x2717,
  0x2718,
  0x274c, // crosses
  0x26a0 // warning
]);

function isHandledDingbat(cp) {
  return HANDLED_DINGBATS.has(cp);
}

// --- File enumeration -------------------------------------------------------

function walk(dir, predicate, out) {
  let entries;
  try {
    entries = fs.readdirSync(dir, { withFileTypes: true });
  } catch (error) {
    return;
  }
  for (const entry of entries) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      if (EXCLUDE_DIRS.has(entry.name)) continue;
      walk(full, predicate, out);
    } else if (entry.isFile()) {
      if (predicate(full)) out.push(full);
    }
  }
}

function defaultFileSet() {
  const out = [];
  walk(
    ROOT_DIR,
    (p) => {
      if (GENERATED_FILE_PATHS.has(p)) return false;
      return p.endsWith(".md");
    },
    out
  );
  for (const root of CS_SCAN_ROOTS) {
    const abs = path.join(ROOT_DIR, root);
    if (!fs.existsSync(abs)) continue;
    walk(abs, (p) => p.endsWith(".cs"), out);
  }
  return out;
}

// --- Core logic -------------------------------------------------------------

/**
 * Replace each arrow character one-at-a-time, choosing the replacement based
 * on the surrounding text in the (already partially-substituted) string.
 */
function substituteArrows(line) {
  let out = line;
  out = replaceWithContext(out, ARROW_RIGHT_RE, chooseRightArrowReplacement);
  out = replaceWithContext(out, ARROW_LEFT_RE, chooseLeftArrowReplacement);
  out = replaceWithContext(out, ARROW_BOTH_RE, chooseBothArrowReplacement);
  out = out.replace(ARROW_UP_RE, "^");
  return out;
}

/**
 * Replace all matches of `pattern` in `str`, calling `chooser(currentString,
 * matchOffset)` to compute each replacement based on the up-to-date version
 * of the string (so earlier replacements affect later context).
 */
function replaceWithContext(str, pattern, chooser) {
  let out = str;
  while (true) {
    pattern.lastIndex = 0;
    const match = pattern.exec(out);
    if (!match) return out;
    const offset = match.index;
    const replacement = chooser(out, offset);
    out = out.slice(0, offset) + replacement + out.slice(offset + match[0].length);
  }
}

function chooseRightArrowReplacement(line, offset) {
  const before = line.slice(0, offset);
  const after = line.slice(offset + 1);

  // Inside markdown link text "[...]" -- drop the arrow rather than emit
  // text that breaks the visible label.
  if (isInsideBrackets(before, after)) {
    return "";
  }

  const beforeTrim = before.replace(/\s+$/, "");
  const afterTrim = after.replace(/^\s+/, "");
  const lastBefore = beforeTrim.slice(-1);
  const firstAfter = afterTrim.slice(0, 1);

  // Empty before context -- avoid leading ">".
  if (beforeTrim.length === 0) {
    return "";
  }

  const menuLikeBefore = /[A-Za-z0-9_)\]"'`]/.test(lastBefore);
  const menuLikeAfter = /[A-Za-z0-9_(\["'`]/.test(firstAfter);

  if (menuLikeBefore && afterTrim.length === 0) {
    return /\s$/.test(before) ? "->" : " ->";
  }
  if (menuLikeBefore && menuLikeAfter) {
    return " -> ";
  }
  return " to ";
}

function chooseLeftArrowReplacement(line, offset) {
  const before = line.slice(0, offset);
  const after = line.slice(offset + 1);

  if (isInsideBrackets(before, after)) {
    return "";
  }

  const beforeTrim = before.replace(/\s+$/, "");
  const afterTrim = after.replace(/^\s+/, "");
  const lastBefore = beforeTrim.slice(-1);
  const firstAfter = afterTrim.slice(0, 1);

  const menuLikeBefore = /[A-Za-z0-9_)\]"'`]/.test(lastBefore);
  const menuLikeAfter = /[A-Za-z0-9_(\["'`]/.test(firstAfter);

  if (menuLikeBefore && menuLikeAfter) {
    return " <- ";
  }
  return " from ";
}

function chooseBothArrowReplacement(line, offset) {
  const before = line.slice(0, offset);
  const after = line.slice(offset + 1);

  if (isInsideBrackets(before, after)) {
    return "";
  }
  return " <-> ";
}

/**
 * True when offset lies inside a "[...]" bracket pair on the same line, with
 * no intervening "]" closing the bracket before the offset.
 */
function isInsideBrackets(before, after) {
  const lastOpen = before.lastIndexOf("[");
  if (lastOpen === -1) return false;
  const lastClose = before.lastIndexOf("]");
  if (lastClose > lastOpen) return false;
  const closeAhead = after.indexOf("]");
  return closeAhead !== -1;
}

/**
 * Apply check/cross/warning substitutions with context awareness.
 * Tables (lines containing "|") and checklist items get word/checkbox forms;
 * other prose drops the symbols entirely.
 */
function applyCheckCrossWarning(line) {
  let out = line;

  // Checklist context: "- ✓" or "* ✗" at line start.
  out = out.replace(/^(\s*[-*]\s+)[✓✅]\s*/u, "$1[x] ");
  out = out.replace(/^(\s*[-*]\s+)[✗✘❌]\s*/u, "$1[ ] ");

  if (/\|/.test(out)) {
    // Table context: words.
    out = out.replace(CHECK_RE, "Yes");
    out = out.replace(CROSS_RE, "No");
    out = out.replace(/^(\s*\|?\s*)⚠️?\s*/u, "$1Warning: ");
    out = out.replace(WARNING_RE, "");
  } else {
    // Prose context: drop the symbols (callers should already have
    // alternative phrasing).
    out = out.replace(CHECK_RE, "");
    out = out.replace(CROSS_RE, "");
    out = out.replace(/^(\s*)⚠️?\s*/u, "$1Warning: ");
    out = out.replace(WARNING_RE, "");
  }
  return out;
}

/**
 * Apply unconditional and prose-only substitutions to a single line.
 *
 * @param {string} line       Line content (no trailing newline).
 * @param {boolean} inFence   True if currently inside a fenced code block.
 * @param {boolean} arrowAware Apply arrow substitutions (off inside fences).
 */
function rewriteLine(line, inFence, arrowAware) {
  let out = line;

  for (const sub of UNCONDITIONAL_SUBS) {
    out = out.replace(sub.from, sub.to);
  }

  if (!inFence) {
    for (const sub of PROSE_ONLY_SUBS) {
      out = out.replace(sub.from, sub.to);
    }
    if (arrowAware) {
      out = substituteArrows(out);
      // Collapse the doubled-space artifacts that result when an em-dash
      // or other punctuation already left a space adjacent to the arrow
      // chooser's added " -> " / " <- " / " <-> ". This is prose-only and
      // does not run inside fenced code blocks (where shifts/comparisons
      // need exact spacing).
      out = out.replace(/\s{2,}->\s{2,}/g, " -> ");
      out = out.replace(/\s{2,}<-\s{2,}/g, " <- ");
      out = out.replace(/\s{2,}<->\s{2,}/g, " <-> ");
    }
    out = applyCheckCrossWarning(out);
  } else {
    // Inside fences we still strip arrows but use simple ASCII forms with
    // no contextual logic so we don't alter code semantics.
    out = out.replace(ARROW_RIGHT_RE, "->");
    out = out.replace(ARROW_LEFT_RE, "<-");
    out = out.replace(ARROW_BOTH_RE, "<->");
    out = out.replace(ARROW_UP_RE, "^");
    out = out.replace(CHECK_RE, "");
    out = out.replace(CROSS_RE, "");
    out = out.replace(WARNING_RE, "");
  }

  const issues = [];
  for (let i = 0; i < out.length; ) {
    const cp = out.codePointAt(i);
    const ch = String.fromCodePoint(cp);
    i += ch.length;
    if (cp < 0x80) continue;
    if (cp === 0xfeff) continue;
    if (cp === 0xfe0f) continue;
    if (cp >= 0x1f300) continue;
    if (BOX_DRAWING_RE.test(ch)) {
      issues.push({ kind: "box-drawing", char: ch, codepoint: cp, column: i });
      continue;
    }
    if (DINGBAT_RANGE_RE.test(ch) && !isHandledDingbat(cp)) {
      issues.push({ kind: "dingbat", char: ch, codepoint: cp, column: i });
      continue;
    }
  }

  return { line: out, issues };
}

function isFenceLine(line) {
  return /^\s*(```|~~~)/.test(line);
}

function processMarkdown(content) {
  const lines = content.replace(/\r\n/g, "\n").replace(/\r/g, "\n").split("\n");
  const issues = [];
  let inFence = false;
  let changed = false;
  let emojiCount = 0;

  for (let i = 0; i < lines.length; i++) {
    const original = lines[i];
    if (isFenceLine(original)) {
      const result = rewriteLine(original, /* inFence */ true, /* arrowAware */ false);
      if (result.line !== original) changed = true;
      for (const issue of result.issues) {
        issues.push({ ...issue, line: i + 1 });
      }
      lines[i] = result.line;
      inFence = !inFence;
      continue;
    }
    const result = rewriteLine(original, inFence, /* arrowAware */ !inFence);
    if (result.line !== original) changed = true;
    for (const issue of result.issues) {
      issues.push({ ...issue, line: i + 1 });
    }
    lines[i] = result.line;
    emojiCount += countEmojis(result.line);
  }

  return { content: lines.join("\n"), changed, issues, emojiCount };
}

function processCSharp(content) {
  const lines = content.replace(/\r\n/g, "\n").replace(/\r/g, "\n").split("\n");
  const issues = [];
  let changed = false;

  for (let i = 0; i < lines.length; i++) {
    const original = lines[i];
    const trimmed = original.replace(/^\s+/, "");
    if (!trimmed.startsWith("///")) {
      // Skip non-doc lines silently. C# string literals legitimately
      // contain non-ASCII chars in some test fixtures.
      continue;
    }
    const result = rewriteLine(original, /* inFence */ false, /* arrowAware */ true);
    if (result.line !== original) changed = true;
    for (const issue of result.issues) {
      issues.push({ ...issue, line: i + 1 });
    }
    lines[i] = result.line;
  }

  return { content: lines.join("\n"), changed, issues };
}

function countEmojis(line) {
  let n = 0;
  for (let i = 0; i < line.length; ) {
    const cp = line.codePointAt(i);
    const ch = String.fromCodePoint(cp);
    i += ch.length;
    if (cp >= 0x1f300 && cp <= 0x1faff) n++;
  }
  return n;
}

// --- CLI --------------------------------------------------------------------

function parseArgs(argv) {
  const args = { check: false, paths: null, files: [] };
  for (let i = 0; i < argv.length; i++) {
    const a = argv[i];
    if (a === "--check") {
      args.check = true;
    } else if (a === "--paths") {
      args.paths = argv[++i];
    } else if (a.startsWith("--")) {
      console.error(`Unknown option: ${a}`);
      process.exit(1);
    } else {
      args.files.push(a);
    }
  }
  return args;
}

function resolveFileList(args) {
  if (args.files.length > 0) {
    return args.files.map((f) => path.resolve(process.cwd(), f));
  }
  if (args.paths) {
    const out = [];
    for (const entry of args.paths.split(",")) {
      const abs = path.resolve(process.cwd(), entry);
      if (!fs.existsSync(abs)) continue;
      const stat = fs.statSync(abs);
      if (stat.isDirectory()) {
        walk(
          abs,
          (p) =>
            p.endsWith(".md") ||
            (p.endsWith(".cs") &&
              CS_SCAN_ROOTS.some((root) => p.includes(`${path.sep}${root}${path.sep}`))),
          out
        );
      } else if (stat.isFile()) {
        out.push(abs);
      }
    }
    return out;
  }
  return defaultFileSet();
}

function main() {
  const args = parseArgs(process.argv.slice(2));
  const files = resolveFileList(args);

  let totalChanged = 0;
  let mdChanged = 0;
  let csChanged = 0;
  const boxDrawingHits = [];
  const dingbatHits = [];
  const emojiWarnings = [];

  for (const file of files) {
    let source;
    try {
      source = fs.readFileSync(file, "utf8");
    } catch (error) {
      console.error(`Skipping ${file}: ${error.message}`);
      continue;
    }
    const isMd = file.endsWith(".md");
    const isCs = file.endsWith(".cs");
    if (!isMd && !isCs) continue;

    const result = isMd ? processMarkdown(source) : processCSharp(source);

    for (const issue of result.issues) {
      const rel = path.relative(ROOT_DIR, file);
      const record = `${rel}:${issue.line}:${issue.column} U+${issue.codepoint
        .toString(16)
        .toUpperCase()
        .padStart(4, "0")} ${JSON.stringify(issue.char)}`;
      if (issue.kind === "box-drawing") boxDrawingHits.push(record);
      else if (issue.kind === "dingbat") dingbatHits.push(record);
    }

    if (isMd && typeof result.emojiCount === "number" && result.emojiCount > 5) {
      emojiWarnings.push(`${path.relative(ROOT_DIR, file)}: ${result.emojiCount} emojis`);
    }

    if (result.changed) {
      totalChanged++;
      if (isMd) mdChanged++;
      else if (isCs) csChanged++;
      if (!args.check) {
        fs.writeFileSync(file, result.content);
      }
    }
  }

  if (boxDrawingHits.length > 0) {
    console.error("Box-drawing characters detected (manual rewrite required):");
    for (const h of boxDrawingHits) console.error(`  ${h}`);
  }
  if (dingbatHits.length > 0) {
    console.error("Unhandled dingbat-range characters (U+2300-U+27BF):");
    for (const h of dingbatHits) console.error(`  ${h}`);
  }
  if (emojiWarnings.length > 0) {
    console.warn("Emoji-cap warnings (>5 per file):");
    for (const w of emojiWarnings) console.warn(`  ${w}`);
  }

  if (args.check) {
    if (totalChanged > 0) {
      console.error(
        `--check: ${totalChanged} file(s) would be modified (md=${mdChanged}, cs=${csChanged})`
      );
      process.exit(1);
    }
    if (boxDrawingHits.length > 0) process.exit(2);
    process.exit(0);
  }

  console.log(
    `Normalized ${totalChanged} file(s) (md=${mdChanged}, cs=${csChanged}); ` +
      `box-drawing=${boxDrawingHits.length}, unhandled-dingbats=${dingbatHits.length}, ` +
      `emoji-warnings=${emojiWarnings.length}`
  );

  if (boxDrawingHits.length > 0) process.exit(2);
  process.exit(0);
}

if (require.main === module) {
  main();
}

module.exports = {
  processMarkdown,
  processCSharp,
  rewriteLine
};
