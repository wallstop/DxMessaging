#!/usr/bin/env node

"use strict";

const fs = require("fs");
const path = require("path");

const ROOT_DIR = path.resolve(__dirname, "..");
const OUTPUT_RELATIVE_PATH = ".operator-runbooks/ambiguous-release-setup.md";
const TRACKED_RUNBOOK_SOURCES = [
  {
    title: "Ambiguous release migration operator guide",
    relativePath: "docs/ops/ambiguous-release-migration.md"
  },
  {
    title: "Release operations",
    relativePath: "docs/ops/release-operations.md"
  },
  {
    title: "GitHub transfer",
    relativePath: "docs/ops/github-transfer.md"
  },
  {
    title: "CI and GitHub settings",
    relativePath: "docs/ops/ci-and-github-settings.md"
  },
  {
    title: "npm release publishing",
    relativePath: "docs/ops/npm-release-publishing.md"
  },
  {
    title: "OpenUPM metadata",
    relativePath: "docs/ops/openupm-metadata.md"
  },
  {
    title: "Unity Asset Store UPM",
    relativePath: "docs/ops/unity-asset-store-upm.md"
  },
  {
    title: "Post-transfer verification",
    relativePath: "docs/ops/post-transfer-verification.md"
  }
];

function generateRunbookContent() {
  return `# Ambiguous Release Setup Runbook

This ignored local runbook is generated by \`scripts/generate-ambiguous-release-runbook.js\`.

Use it only for non-sensitive public execution notes during the Ambiguous release migration. Do not paste secrets, account screenshots, publisher account identifiers, recovery codes, tokens, private account metadata, private contact details, publisher portal notes, provider configuration state, private review or approval details, draft-release links, or sensitive local status into tracked files or this local runbook.

Tracked source documents to follow:

${TRACKED_RUNBOOK_SOURCES.map((source) => `- \`${source.relativePath}\``).join("\n")}

## Public Verification Notes

- GitHub repository URL:
- GitHub Pages URL:
- Latest public release tag:
- npm package URL:
- OpenUPM package URL:
- Public OpenUPM PR URL:
- Public Unity Asset Store listing URL, if published:

## Public Follow-Up Links

- Public issue or PR:
- Public release notes:
- Public documentation update:

## Non-Sensitive Next Actions

- [ ] 
`;
}

function generateRunbook(options = {}) {
  const rootDir = options.rootDir || ROOT_DIR;
  const outputPath = options.outputPath || path.join(rootDir, OUTPUT_RELATIVE_PATH);
  const force = options.force === true;
  const content = generateRunbookContent();

  fs.mkdirSync(path.dirname(outputPath), { recursive: true });
  try {
    fs.writeFileSync(outputPath, content, {
      encoding: "utf8",
      flag: force ? "w" : "wx"
    });
  } catch (error) {
    if (!force && error && error.code === "EEXIST") {
      const runbookExistsError = new Error(
        `${path.relative(rootDir, outputPath).split(path.sep).join("/")} already exists. Re-run with --force to overwrite.`
      );
      runbookExistsError.code = "RUNBOOK_EXISTS";
      throw runbookExistsError;
    }

    throw error;
  }

  return outputPath;
}

function main(argv = process.argv.slice(2)) {
  if (argv.includes("--help") || argv.includes("-h")) {
    console.log("Usage: node scripts/generate-ambiguous-release-runbook.js [--force]");
    console.log(`Writes ${OUTPUT_RELATIVE_PATH} from tracked template content.`);
    console.log("By default, refuses to overwrite an existing runbook.");
    return 0;
  }

  let force = false;
  for (const arg of argv) {
    if (arg === "--force") {
      force = true;
      continue;
    }

    console.error(`Unknown argument: ${arg}`);
    return 1;
  }

  let outputPath;
  try {
    outputPath = generateRunbook({ force });
  } catch (error) {
    if (error && error.code === "RUNBOOK_EXISTS") {
      console.error(error.message);
      return 1;
    }

    throw error;
  }

  console.log(`Generated ${path.relative(ROOT_DIR, outputPath).split(path.sep).join("/")}`);
  return 0;
}

if (require.main === module) {
  process.exitCode = main();
}

module.exports = {
  OUTPUT_RELATIVE_PATH,
  TRACKED_RUNBOOK_SOURCES,
  generateRunbook,
  generateRunbookContent,
  main
};
